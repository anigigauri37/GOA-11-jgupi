# for loop'ით და while loop'ით ეცადეთ გამოიტანოთ რიცხვები 1'დან 100-ის ჩათვლით

for i in range(101):
    print(i)

# for ციკლის მეშვეობით შეკრიბეთ პირველი 10 რიცხვი

for i in range(1, 10):
    print("1 + ", i, "=", 1 + i)

#or ციკლის მეშვეობით გამოიტანეთ ლუწი რიცხვები 1'დან 20'ის ჩათვლით

for i in range(22):
    if i % 2 == 0:
        print(i)
